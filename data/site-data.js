function hashString(value) {
  let hash = 0;

  for (let i = 0; i < value.length; i += 1) {
    hash = (hash * 31 + value.charCodeAt(i)) >>> 0;
  }

  return hash;
}

function pickFrom(list, seed, offset = 0) {
  return list[(seed + offset) % list.length];
}

function pickMany(list, seed, count) {
  const chosen = [];
  const seen = new Set();
  let cursor = seed;

  while (chosen.length < Math.min(count, list.length)) {
    const index = cursor % list.length;

    if (!seen.has(index)) {
      seen.add(index);
      chosen.push(list[index]);
    }

    cursor += 3;
  }

  return chosen;
}

function getSeed(serviceSlug, locationSlug = citySlug) {
  return hashString(`${serviceSlug}-${locationSlug}`);
}

